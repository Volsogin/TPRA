package day5;

public class Car {
    private String model;
    private String color;
    private int data;

    public void setModel(String AutoModel) {
        model = AutoModel;
    }

    public String getModel() {
        return model;
    }

    public void setColor(String AutoColor) {
        color = AutoColor;
    }

    public String getColor() {
        return color;
    }

    public void setData(int AutoData) {
        data = AutoData;
    }

    public int getData() {
        return data;
    }
}
